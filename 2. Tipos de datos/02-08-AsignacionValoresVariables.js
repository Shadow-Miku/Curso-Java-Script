// Declaración e inicialización de una variable con `let`
let nombreCompleto = "Juan Perez";
console.log(nombreCompleto); // "Juan Perez"
// Aquí, `nombreCompleto` es una variable de tipo string que se inicializa con el valor "Juan Perez"

// Declaración de múltiples variables con `let`
let x, y; // Declaración de las variables `x` y `y` sin inicializarlas
x = 10; // Asignación del valor 10 a la variable `x`
y = 20; // Asignación del valor 20 a la variable `y`

// Uso de las variables para realizar una operación
let z = x + y; // Declaración e inicialización de la variable `z` con la suma de `x` y `y`
console.log(z); // 30
// Aquí, `z` es una variable que almacena el resultado de la suma de `x` e `y`, que es 30
