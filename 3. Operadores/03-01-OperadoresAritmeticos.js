/*
Los operadores aritméticos en JavaScript se utilizan para realizar cálculos matemáticos. Aquí hay una lista de los operadores aritméticos más comunes:

1. Suma (+): Suma dos números.
2. Resta (-): Resta un número de otro.
3. Multiplicación (*): Multiplica dos números.
4. División (/): Divide un número por otro.
5. Módulo (%): Devuelve el residuo de una división.
6. Exponente (**): Eleva un número a la potencia de otro.
*/

let a = 3,
  b = 2; // Declaración e inicialización de las variables `a` y `b`

// Operador de suma (+)
let z = a + b;
console.log("Resultado de la suma: " + z); // Resultado de la suma: 5

// Operador de resta (-)
z = a - b;
console.log("Resultado de la resta: " + z); // Resultado de la resta: 1

// Operador de multiplicación (*)
z = a * b;
console.log("Resultado de la multiplicación: " + z); // Resultado de la multiplicación: 6

// Operador de división (/)
z = a / b;
console.log("Resultado de la división: " + z); // Resultado de la división: 1.5

// Operador de módulo (%)
z = a % b; // Residuo de la división de `a` entre `b`
console.log("Resultado de la operación módulo (residuo): " + z); // Resultado de la operación módulo (residuo): 1

// Operador de exponente (**)
z = a ** b;
console.log("Resultado del operador exponente: " + z); // Resultado del operador exponente: 9
