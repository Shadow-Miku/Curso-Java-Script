// Sentencia if-else en detalle y profundidad
/*
La sentencia `if-else` en JavaScript se utiliza para tomar decisiones basadas en condiciones. Permite ejecutar un bloque de código si una condición es verdadera y otro bloque de código si la condición es falsa.

Sintaxis:
if (condicion) {
  // Bloque de código a ejecutar si la condición es verdadera
} else {
  // Bloque de código a ejecutar si la condición es falsa
}
*/

// Ejemplo: Uso de la sentencia if-else
let edad = 20;

if (edad >= 18) {
  console.log("Eres mayor de edad."); // Se ejecuta si la condición (edad >= 18) es verdadera
} else {
  console.log("Eres menor de edad."); // Se ejecuta si la condición (edad >= 18) es falsa
}
